function drawSpiral(size) {
  if (size <= 1 || typeof size != "number")
    return console.log("Parámetro no válido")

  let row = ""
  let verticalLeftLines, verticalRightLines, horizontalLines

  for (let i = 0; i < size; i++) {
    if (i < size / 2) {
      cornetLeft = "╔"
      cornerRight = "╗"
      verticalLeftLines = Math.max(0, i - 1)
      verticalRightLines = i
    } else {
      cornetLeft = "╚"
      cornerRight = "╝"
      verticalLeftLines = Math.max(0, size - i - 1)
      verticalRightLines = size - i - 1
    }

    horizontalLines = size - 2 - verticalLeftLines - verticalRightLines
    row =
      "║".repeat(verticalLeftLines) +
      (i === 0 ? "═" : cornetLeft) +
      "═".repeat(horizontalLines) +
      cornerRight +
      "║".repeat(verticalRightLines)

    console.log(row)
  }
}

